/*
Write a function called calculateArea that takes the radius of a circle as a parameter and returns the area of the circle. 
Remember, the formula to calculate the area of a circle is πr^2, where 'r' is the radius of the circle and π is a mathematical constant approximately equal to 3.14.
*/

const calculateArea = (radius) => Math.PI * radius * radius;
console.log(calculateArea(5));
